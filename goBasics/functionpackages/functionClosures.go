package main

import "fmt"

//closure is a function value that referneces variables from outisde its body
//adder function returns a closure - each closure bound to its own sum variable

func adder() func(int) int {
	sum := 0
	return func(x int) int {
		sum += x
		return sum
	}
}

func main() {
	pos, neg := adder(), adder()
	for i := 0; i < 10; i++ {
		fmt.Println(
			pos(i),
			neg(-2*i),
		)
	}
}
